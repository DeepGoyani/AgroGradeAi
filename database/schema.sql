-- =============================================================================
-- FUNCTIONS & TRIGGERS
-- =============================================================================

-- Auto-update updated_at timestamp
CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ language 'plpgsql';

CREATE TRIGGER update_farmers_updated_at
    BEFORE UPDATE ON farmers
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

CREATE TRIGGER update_listings_updated_at
    BEFORE UPDATE ON marketplace_listings
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

CREATE TRIGGER update_transactions_updated_at
    BEFORE UPDATE ON transactions
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

-- =============================================================================
-- VIEWS - Useful aggregations
-- =============================================================================

-- Disease statistics by region
CREATE VIEW regional_disease_stats AS
SELECT 
    f.region,
    ai.detected_crop,
    ai.disease,
    COUNT(*) as case_count,
    AVG(ai.disease_confidence) as avg_confidence,
    AVG(ai.severity_percent) as avg_severity
FROM ai_inferences ai
JOIN farmers f ON ai.farmer_id = f.id
WHERE ai.disease IS NOT NULL
    AND ai.created_at > NOW() - INTERVAL '30 days'
GROUP BY f.region, ai.detected_crop, ai.disease
ORDER BY case_count DESC;

-- Farmer dashboard stats
CREATE VIEW farmer_stats AS
SELECT 
    f.id as farmer_id,
    f.name,
    COUNT(DISTINCT ai.id) as total_scans,
    COUNT(DISTINCT CASE WHEN ai.is_healthy = false THEN ai.id END) as disease_detections,
    COUNT(DISTINCT ml.id) as active_listings,
    COALESCE(SUM(t.total_amount) FILTER (WHERE t.seller_id = f.id AND t.status = 'completed'), 0) as total_earnings
FROM farmers f
LEFT JOIN ai_inferences ai ON f.id = ai.farmer_id
LEFT JOIN marketplace_listings ml ON f.id = ml.farmer_id AND ml.status = 'active'
LEFT JOIN transactions t ON f.id = t.seller_id
GROUP BY f.id, f.name;
